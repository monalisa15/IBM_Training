package com.ibm.example;

public class Example {
	

	public int add(int a, int b) {

		return a+b;

	}

	

	public int subs(int a, int b) {

		return a-b;

	}

	

	public int div(int a, int b) {

		return a/b;

	}

	

	public int square(int a) {

		return a*a;

	}
	public int cube(int a)
	{
		return a*a*a;
	}


}
